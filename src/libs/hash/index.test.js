import md5 from "./md5";

test("md5", () => {
  expect(new Uint8Array(md5(new TextEncoder().encode("test")))).toStrictEqual(
    // 098f6bcd4621d373cade4e832627b4f6
    new Uint8Array([
      0x09, 0x8f, 0x6b, 0xcd, 0x46, 0x21, 0xd3, 0x73, 0xca, 0xde, 0x4e, 0x83, 0x26, 0x27, 0xb4,
      0xf6,
    ]),
  );

  expect(new Uint8Array(md5(new TextEncoder().encode("hello world")))).toStrictEqual(
    // 5eb63bbbe01eeed093cb22bb8f5acdc3
    new Uint8Array([
      0x5e, 0xb6, 0x3b, 0xbb, 0xe0, 0x1e, 0xee, 0xd0, 0x93, 0xcb, 0x22, 0xbb, 0x8f, 0x5a, 0xcd,
      0xc3,
    ]),
  );
});
